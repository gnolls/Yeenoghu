<h1>What is Dungeon Organizer?</h1>

<p>Dungeon Organizer is an interactive website for players of Dungeons & Dragons 3.5, that allows for:</p>

<ul style = "list-style-type:square; margin-left: 25px">

	<li>reation/Management of Character Sheets.</li>

	<li>Searching for spells, items, races, classes, feats, etc.</li>

	<li>Creation of parties comprised of a Dungeon Master and players.</li>

	<li>Conversation and long distance play via a Bulletin Board.</li>

</ul>

<h1>Why Dungeon Organizer?</h1>

<p>The motivation behind the project is the lack of Dungeons and Dragons websites dedicated to providing a personal spellbook and character creation for forum based play with players across the country. Using this website would open communication between people that share an interest in D&D that would otherwise not likely be established.</p>
